using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;


    class Sumof3Nums
    {
        static void Main()
        {
            Console.WriteLine("Integer A is ? ... ");
            int IntegerA = int.Parse(Console.ReadLine());
            Console.WriteLine("Integer B is ? ... ");
            int IntegerB = int.Parse(Console.ReadLine());
            Console.WriteLine("Integer C is ? ... ");
            int IntegerC = int.Parse(Console.ReadLine());
            Console.WriteLine("The sum of {0}, {1} and {2} is {3}.", IntegerA, IntegerB, IntegerC, IntegerA + IntegerB + IntegerC);


        }
    }

